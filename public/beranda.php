<h2>Selamat Datang</h2>
<p style="text-align: justify; text-indent: 0.3in;">Sistem Pakar (dalam bahasa inggris <i>Expert system</i>) adalah sistem informasi yang berisi dengan pengetahuan
                    dari pakar sehingga dapat digunakan untuk konsultasi. pengetahuan dari pakar di dalam sustem ini digunakan sebagai dasar oleh 
                    Sistem Pakar untuk menjawab pertanyaan(konsultasi). 
</p>

<p style="text-align: justify; text-indent: 0.3in;">Virus adalah makhluk mikroskopis yang seringkali dianggap merugikan kehidupan makhluk hidup. 
   Virus dapat menginveksi berbagai kehidupan lain baik manusia, hewan, tumbuhan, bahkan juga bakteri. 
   Virus dianggap merugikan karena sifatnya yang memanfaatkan materi sel yang ditempatinya di dalam sel hidup. 
   Ini disebabkan virus tidak dapat berkembang biak di luar sel hidup. Karenanya, virus seringkali berpotensi menyebabkan suatu penyakit.
</p>

<p style="text-align: justify; text-indent: 0.3in;">Dalam aplikasi ini sistem pakar digunakan untuk mendiagnosa penyakit yang disebabkan oleh virus pada manusia dengan menggunkana <i>Metode Teorema Bayes</i></p>
  